

let block = document.querySelector('div.block');

function showBlock() {
    block.style.display = "flex"
};
function hideBlock() {
    block.style.display = "none"
};


let list = document.querySelector('ul.listColor');

function showList() {
    list.style.display = "flex"
};
function hideList() {
    list.style.display = "none"
};

